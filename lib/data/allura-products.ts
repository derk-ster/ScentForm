const currencyCode = "USD" as const;

const defaultClaims: ProductClaims = {
  crueltyFree: true,
  phthalateFree: true,
  parabenFree: true,
  dyeFree: true,
  recyclablePackaging: true,
  madeInUSA: true,
};

function oneVariant(
  handle: string,
  concentrationLabel: string,
  sizeLabel: string,
  priceDollars: number,
  sizeMl = 50,
): ProductVariant[] {
  return [
    {
      id: `${handle}-std`,
      sku: `A7-${handle.toUpperCase().replace(/-/g, "-")}`,
      concentration: "edp",
      concentrationLabel,
      sizeLabel,
      sizeMl,
      priceCents: Math.round(priceDollars * 100),
      currencyCode,
      available: true,
    },
  ];
}

function collectionForCategory(cat: PrimaryCategoryHandle): {
  collectionHandle: string;
  collectionTitle: string;
} {
  switch (cat) {
    case "perfumes-colognes":
      return { collectionHandle: "a7-personal", collectionTitle: "Personal fragrance" };
    case "body":
      return { collectionHandle: "a7-body", collectionTitle: "Body care" };
    case "home":
      return { collectionHandle: "a7-home", collectionTitle: "Home scent" };
    case "incense":
      return { collectionHandle: "a7-incense", collectionTitle: "Incense" };
    case "diffuser-machines":
      return {
        collectionHandle: "a7-diffuser-hardware",
        collectionTitle: "Diffuser machines",
      };
    case "diffuser-oils":
      return { collectionHandle: "a7-diffuser-oils", collectionTitle: "Diffuser oils" };
    case "gift-sets":
      return { collectionHandle: "a7-gifts", collectionTitle: "Gift sets" };
    default:
      return { collectionHandle: "a7-personal", collectionTitle: "ALLURA 7" };
  }
}

function categoryTitle(cat: PrimaryCategoryHandle): string {
  const titles: Record<PrimaryCategoryHandle, string> = {
    "perfumes-colognes": "Perfumes & Colognes",
    body: "Body Products",
    home: "Home Products",
    incense: "Incense",
    "diffuser-machines": "Diffuser Machines",
    "diffuser-oils": "Diffuser Oils",
    "gift-sets": "Gift Sets",
  };
  return titles[cat];
}

function buildProduct(input: {
  handle: string;
  title: string;
  primaryCategory: PrimaryCategoryHandle;
  productTypeLabel: string;
  description: string;
  priceDollars: number;
  concentrationLabel?: string;
  sizeLabel?: string;
  sizeMl?: number;
  notes: NotePyramid;
  profile: FragranceProfile;
  specifications: ProductSpecifications;
  listingKind: "fragrance" | "lifestyle";
  subtitle?: string;
  tagline?: string;
  gender?: ProductGender;
  scentFamilies?: ScentFamilyTag[];
  isNew?: boolean;
  isBestSeller?: boolean;
  isSignature?: boolean;
  limitedEdition?: boolean;
  featured?: boolean;
  giftable?: boolean;
  relatedHandles?: string[];
}): Product {
  const { collectionHandle, collectionTitle } = collectionForCategory(
    input.primaryCategory,
  );
  const imgA = linePhoto(input.handle, 2);
  const imgB = linePhotoThumb(input.handle);
  const concLabel =
    input.concentrationLabel ?? input.productTypeLabel;
  const sizeLabel = input.sizeLabel ?? "One size";
  return {
    id: input.handle,
    handle: input.handle,
    title: input.title,
    subtitle: input.subtitle,
    tagline: input.tagline,
    collectionHandle,
    collectionTitle,
    primaryCategory: input.primaryCategory,
    categoryTitle: categoryTitle(input.primaryCategory),
    productTypeLabel: input.productTypeLabel,
    listingKind: input.listingKind,
    brand: "ALLURA 7",
    description: input.description,
    notes: input.notes,
    profile: input.profile,
    specifications: input.specifications,
    claims: defaultClaims,
    variants: oneVariant(
      input.handle,
      concLabel,
      sizeLabel,
      input.priceDollars,
      input.sizeMl,
    ),
    images: [imgA, imgB],
    hoverImage: imgB,
    featured: input.featured ?? false,
    isNew: input.isNew ?? false,
    isBestSeller: input.isBestSeller,
    isSignature: input.isSignature,
    limitedEdition: input.limitedEdition,
    gender: input.gender,
    scentFamilies: input.scentFamilies,
    giftable: input.giftable ?? true,
    hasTravelSizes: false,
    relatedHandles: input.relatedHandles,
  };
}

